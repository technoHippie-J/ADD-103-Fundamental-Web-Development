# Common Exceptions:

# SyntaxError: Errors in syntax, often due to typos or incorrect structure.
# NameError: Occurs when a variable name is not found.
# TypeError: Raised when an operation is applied to an object of inappropriate type.
# IndexError: Occurs when you try to access an index out of the range of a list.
# KeyError: Raised when a dictionary key is not found.
# ValueError: Occurs when a function gets an argument of correct type but improper value.
# AttributeError: When an attribute reference or assignment fails.
# ZeroDivisionError: When a division or modulo by zero takes place.
# IOError: An error raised during input/output operations, like file not found.
# ImportError: Occurs when an imported module or object cannot be found.
# StopIteration: Raised by next() to indicate that there is no further item to be returned by iterator.
# FileNotFoundError: When a file or directory is requested but doesn't exist.
# OverflowError: When an arithmetic operation is too large to be represented.
# MemoryError: When an operation runs out of memory.
# RecursionError: Occurs when the maximum recursion depth has been exceeded.
